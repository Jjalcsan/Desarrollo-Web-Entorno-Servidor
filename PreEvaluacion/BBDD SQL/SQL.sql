CREATE TABLE PROFESORES (
NOMBRECOMPLETO		VARCHAR2(60),
DNI					VARCHAR2(8) PRIMARY KEY NOT NULL,
DIRECCION			VARCHAR2(40),
TITULO				VARCHAR2(40),
GANA				NUMBER(6)
);

CREATE TABLE CURSOS (
NOMBRE_CURSO		VARCHAR2(30),
COD_CURSO			VARCHAR2(10) PRIMARY KEY NOT NULL,
DNI_PROFESOR		VARCHAR2(8) REFERENCES PROFESORES NOT NULL,
MAXIMO_ALUMNOS		NUMBER(2),
NUM_HORAS			NUMBER(4)
);

CREATE TABLE AULA (
NOMBRE				VARCHAR2(30),
COD_AULA			VARCHAR2(10) PRIMARY KEY NOT NULL,
COD_CURSO			VARCHAR2(10) REFERENCES CURSOS NOT NULL,
ESPACIO				NUMBER(3)
);

CREATE TABLE ALUMNOS (
NOMBRECOMPLETO		VARCHAR2(60) NOT NULL,
DNI 				VARCHAR2(8) PRIMARY KEY NOT NULL,
DIRECCION			VARCHAR2(40),
SEXO				VARCHAR2(1) CHECK (SEXO IN('H', 'M')) NOT NULL,
CURSO 				VARCHAR2(10) REFERENCES CURSOS NOT NULL
);

